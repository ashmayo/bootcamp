#class = what types of object the thing is

puts "The class of 3.5 is #{(3.5).class}" #will evaluate to float

puts "The class of String.new is #{String.new.class}" #will evaluate to string

puts "The class of String is #{String.class}" #will evaluate to class 

puts "The class of 'hello'.class is #{'hello'.class}" #will evaluate to string 